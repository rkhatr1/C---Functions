#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	int len;

	i = 0;
	if (argc == 2)
	{
		while (argv[1][i] != '\0')
		{
			i++;
		}
		len = i;
		while (len >= 0)
		{
			write(1, &argv[1][len], 1);
			len--;
		}
		write(1, "\n", 1);
	}
	else
	{
		write(1, "\n", 1);
	}
}
