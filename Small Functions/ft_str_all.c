#include <unistd.h>
#include <stdio.h>

char	*ft_strcpy(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s2[i] != '\0')
	{
		s1[i] = s2[i];
		i++;
	}
	while (s1[i] != '\0')
	{
		s1[i] = '\0';
	}
	return (s1);
}

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

void	ft_swap(int *a, int *b)
{
	int temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

void	ft_print_numbers(void)
{
	write(1, "0123456789\n", 10);
}

int		main(void)
{
	write(1, "Hello World!\n", 12);
	ft_print_numbers();
	return (0);
}
