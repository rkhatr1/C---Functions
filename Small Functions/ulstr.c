#include <unistd.h>

int		main(int ac, char **av)
{
	int	i;
	char new;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			if (av[1][i] >= 'a' && av[1][i] <= 'z')
				new = av[1][i] - 32;
			else if (av[1][i] >= 'A' && av[1][i] <= 'Z')
				new = av[1][i] + 32;
			if ((av[1][i] >= 'a' && av[1][i] <= 'z') ||
					(av[1][i] >= 'A' && av[1][i] <= 'Z'))
				write(1, &new, 1);
			else
				write(1, &av[1][i], 1);
			i++;
		}
	}
	else
		write(1, "\n", 1);
		return (0);
}
