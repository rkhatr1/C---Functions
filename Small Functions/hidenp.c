#include <unistd.h>

int		main(int ac, char **av)
{
	int k;
	int j;

	k = 0;
	j = 0;
	if (ac == 3)
	{
		while (av[1][k] != '\0' && av[2][j] != '\0')
		{
			if (av[1][k] == av[2][j])
				k++;
			j++;
		}
		if (av[1][k] == '\0')
			write(1, "1", 1);
		else
			write(1, "0", 1);
	}
	write(1, "\n", 1);
	return (0);
}
